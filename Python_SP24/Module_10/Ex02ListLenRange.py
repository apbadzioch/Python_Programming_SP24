# Example 7-2: Using len and range functions

g1 = ['Graham Chapman','Eric Idle','Terry Gilliam']

for i in range(len(g1)):
    print(i , g1[i])

e = 'Eric Idle'
for i in range(len(e)):
    print(i , e[i])

