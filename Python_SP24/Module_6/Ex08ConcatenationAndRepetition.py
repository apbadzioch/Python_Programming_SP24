# Concatenation and Repetition

a = 'Hello' + 'World'
b = 'Hello' 'World'
c = 'Hello'\
    'World'
x = '*' * 50
print(x)
print(a)
print(x)
print(b)
print(x)
print(c)
print(x)
print('Knights' 'Who' 'Say' 'Ni')
print(x)

